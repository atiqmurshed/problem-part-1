let operator = "-";
let num1 = 10;
let num2 = 5;

if (operator === "+") {
  console.log(num1 + num2);
} else if (operator === "-") {
  console.log(num1 - num2);
} else if (operator === "/") {
  console.log(num1 / num2);
} else if (operator === "*") {
  console.log(num1 * num2);
}





